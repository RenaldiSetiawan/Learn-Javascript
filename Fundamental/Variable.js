// Deklasrasi Variable var / let / const

var lastName; //decalaration statement
lastName = "Renal" // mengisi nilai variable dengan tanda =
console.log(lastName);

// Langsung mengisi nilai variable
let yourName = "Setiawan"
console.log(yourName);

// Const variable tidak bisa diubah setelah di inisalisasi nialinya.
// Jika mengigisi mendapat error “TypeError: Assignment to constant variable.”
const number = 100;
console.log(number);

number = 200;
console.log(number);